        <p class="mb-2 font-semibold text-2xl leading-9">Wat is kg CO₂-eq?</p>
        <p class="text-xl leading-9">
            In deze interactive hebben we het soms over kg CO₂-eq, maar wat is
            het eigenlijk?
            <br><br>
            kg CO₂-eq staat voor kilogram <a
            href="https://www.climatepartner.com/nl/kennisbank/woordenlijst/co2-equivalent"
            target="_blank"
            class="underline decoration-black hover:text-[#4D00FF] hover:decoration-[#4D00FF] transition duration-200"
            >CO₂-equivalent</a
        >. Het is een eenheid die
            wordt gebruikt om de impact van verschillende broeikasgassen op het
            klimaat uit te drukken in termen van hun effect, vergeleken met
            koolstofdioxide (CO₂). Omdat sommige gassen (zoals methaan en
            lachgas) veel sterker zijn dan CO₂ in het vasthouden van warmte,
            worden ze omgerekend naar een equivalent in CO₂. Zo kun je
            bijvoorbeeld zeggen dat 1 kg methaan een vergelijkbare impact heeft
            als ongeveer 25 kg CO₂.
            <br><br>
            Het helpt om de klimaatimpact van
            verschillende activiteiten of producten te vergelijken, bijvoorbeeld
            van voedsel, vervoer of energiegebruik.
